// JavaScript Variables


// value = undefined by default
var myFirstVariable;    // undefined


document.write(myFirstVariable); // value = undefined by default


document.write("<br>"); // line break



var myName;         // variable declaration

// = is an assignment operator

myName = "Irfan";   // variable initialization
                    
document.write(myName);


document.write("<br>"); // line break


var yourName = "Daniel"; // variable declaration & initialization

document.write(yourName);


document.write("<br>"); // line break


myCar = "BMW"; // declaration & initialization without var keyword

document.write(myCar);


document.write("<br>"); // line break


var x, y, z; // multi variable declartion 

var x = 10, y = 20, z = 30;  // multi variable declartion & initialization


document.write("<br>"); // line break


var serialNumber = 588788;  // Number value assignment

document.write(serialNumber);
















