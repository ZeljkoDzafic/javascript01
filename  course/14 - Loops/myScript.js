// JavaScript Loops

// while... loop
/* 
while() {
    //block of code
}
*/

document.write ("while loop <br>");
var x = 1;

while ( x <= 5 ) {
    
    document.write(x + "<br>");
    
    x++;
}


// do...while loop
document.write ("do...while loop <br>");
var y = 1;

do {
    
    document.write(y + "<br>");
    
    y++;
    
} while(y <= 5);


// for loop
document.write ("for loop <br>");
var z; 

for (  z = 1; z <= 5; ++z ) {
  
    document.write(z + "<br>");  
} 